            sage: L2 = GLattice(PermutationGroup([(2, 3), (1, 6, 3), (7, 6, 4, 5)]), 1)
            sage: L2.GAPMatrixGroup()
            Group([ [ [ 1 ] ], [ [ 1 ] ], [ [ 1 ] ] ])

