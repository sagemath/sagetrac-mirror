            sage: m1 = matrix(3, [0, 0, 1, -1, -1, -1, 1, 0, 0])
            sage: m2 = matrix(3, [-1, 0, 0, 0, -1, 0, 0, 0, -1])
            sage: m3 = matrix(3, [0, 1, 0, 1, 0, 0, -1, -1, -1])
            sage: L3 = GLattice([m1, m2, m3])
