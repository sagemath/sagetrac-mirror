from sage.misc.cachefunc import cached_method
from sage.modules.with_basis.subquotient import SubmoduleWithBasis
from sage.structure.unique_representation import UniqueRepresentation
from sage.categories.finitely_generated_semigroups import FinitelyGeneratedSemigroups
from sage.categories.groups import Groups

class FiniteDimensionalInvariantModule(UniqueRepresentation, SubmoduleWithBasis):
    r"""
    Construct the `G`-invariant submodule of `M`. When a group `G` acts on a module 
    `M`, the invariant module is the collection of elements `x` in `M` such that 
    `g*x = x`.

    ..MATH::

        M^G = \{a \in A : g\cdot a = a}

    NOTE: The current implementation works when `G` is a finitely-generated semigroup,
    and when `A` is a finite-dimensional free module with a distinguished basis.

    TODO::
        Extend when `A` does not have a basis and `G` is a permutation group using:
        - https://arxiv.org/abs/0812.3082
        - https://www.dmtcs.org/pdfpapers/dmAA0123.pdf

    """

    def __init__(self, R, *args, **kwargs):
        """
        INPUTS::

        - ``R`` -- an instance of a ``Representation`` of a semigroup `S` 
                   acting on the module `M`.

        OUTPUTS::

        - ``MS`` -- the invariant algebra of the semigroup action of `S` on `M`, or
                    equivalently, the isotypic component of the representation of 
                    `S` carried by `M` corresponding to the trivial character.

        EXAMPLES::

        TESTS::

            sage: from sage.modules.with_basis.invariant import FiniteDimensionalInvariantModule
            sage: M.<x,y,z> = PolynomialRing(QQ)
            sage: G = SymmetricGroup(3); G.rename('S3')
            sage: import operator
            sage: MG = FiniteDimensionalInvariantModule(M, G, operator.mul); AG
            (S3)-invariant subalgebra of Polynomial Ring over Rational Field

            sage: S = FiniteSemigroups().example(); S
            An example of a finite semigroup: the left regular band generated by ('a', 'b', 'c', 'd')
            sage: FiniteDimensionalInvariantModule()

        """

        self._semigroup = R.semigroup()

        if self._semigroup not in FinitelyGeneratedSemigroups():
            raise ValueError(f'{self._semigroup} is not finitely generated')

        if self._semigroup in Groups():
            self._is_invariant_for_group = True
        else:
            self._is_invariant_for_group = False

        self._ambient_module = R._module
        self._on_ambient_basis = R._on_basis

        side = kwargs.pop('side','left')
        support_order = kwargs.pop('support_order', None)
        unitriangular = kwargs.pop('unitriangular', False)
        category = kwargs.pop('category', None)

        if side == 'left':
            self._on_invariant_basis = lambda g,x: self._on_ambient_basis(g,x) - x
        else:
            self._on_invariant_basis = lambda x,g: self._on_ambient_basis(g,x) - x

        basis = self._ambient_module().annihilator_basis(
                    self._semigroup.gens(),
                    action = self._on_invariant_basis).keys()

        super(SubmoduleWithBasis,self).__init__(basis,
                                                support_order = support_order,
                                                ambient = self._ambient_module,
                                                unitriangular = unitriangular,
                                                category = category,
                                                *args,
                                                **kwargs)

    def _repr_(self):
        """
        EXAMPLES::

            sage: V = VectorSpace(QQ,3)
            sage: G = CyclicPermutationGroup(3)
            sage: FiniteDimensionalInvariantAlgebra(V, G)
            (Cyclic group of order 3 as a permutation group)-invariant subalgebra of Vector space of dimension 3 over Rational Field

        """

        return f"({self._semigroup})-invariant submodule of {self._ambient_module}"


    def semigroup(self):
        """
        Return the semigroup `S` whose action ``self`` is invariant under.

        EXAMPLES::

            sage: G = SymmetricGroup(3)
            sage: A.<x,y,z> = PolynomialRing(QQ)
            sage: import operator
            sage: AG = FiniteDimensionalInvariantAlgebra(G,A,operator.mul)
            sage: AG.group()
            Symmetric group of order 3! as a permutation group

        """

        return self._semigroup

    def group(self):
        """
        When `S` is a group (not just a semigroup), return the group.

        """

        if not self._is_invariant_for_group:
            raise ValueError(f'{self._semigroup} is not a group.')

        return self._semigroup


    def action_on_basis(self,*args):

        if len(args) == 0:
            return self._action_on_basis

        return self._action_on_basis(args)

    class Element(SubmoduleWithBasis.Element):

        # lmul -- self*right
        def _lmul_(self, right):
            """
            EXAMPLES::

                sage:        #### create invariant module
                sage:        #### multiply group element
            """

            if right in self.parent().group() and self.side() == 'right':
                return self

            return None

        # rmul -- left * self
        def _rmul_(self, left):
            """
            EXAMPLES::

                sage: ### create invariant module
                sage: ### multiply group element
            """
            if left in self.parent().group() and self.side() == 'left':
                return self
            
            return None

        def _acted_upon_(self, scalar, self_on_left = False):
            """
            EXAMPLES::

                sage: 
            """

            if scalar in self.parent().group() and self_on_left == (self.side() == 'right'):

                return self

            return None
